%% ===================================================================
%% origami
%% ===================================================================
-module(origami).

%% API
-export([start/0, stop/0]).


%% ===================================================================
%% API
%% ===================================================================
-spec start() -> ok.
start() ->
    {ok, _} = application:ensure_all_started(origami).


-spec stop() -> ok.
stop() ->
    ok = application:stop(origami).
